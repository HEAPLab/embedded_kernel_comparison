int __io_putchar(int ch)
{
  if (ch == '\n') {
    HAL_UART_Transmit(&huart3, (unsigned char *)"\r\n", 2, HAL_MAX_DELAY);
  } else {
    unsigned char c = ch;
    HAL_UART_Transmit(&huart3, &c, 1, HAL_MAX_DELAY);
  }
  return 0;
}

int __io_getchar(void)
{
  unsigned char c;
  HAL_UART_Receive(&huart3, &c, 1, HAL_MAX_DELAY);
  return (int)c;
}

